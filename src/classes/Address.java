package classes;

public class Address {
    String country;
    String region;
    String city;
    String street;
    String house;

    public Address(String country, String region, String city, String street, String house) {
        this.country = country;
        this.region = region;
        this.city = city;
        this.street = street;
        this.house = house;
    }
}
